public class GPScoord {
    private double longitude;
    private double latiude;
    

    public GPScoord(double longitude,double latiude){
        this.longitude = longitude;
        this. latiude = latiude;
        
    }

    double getLongtiude(){
        return longitude;
    }

    double getLatiude(){
        return latiude;
    }

    
}
